    public ImageIcon ResizeImage(String imagePath){
        ImageIcon myImage = new ImageIcon(imagePath);
        Image img = myImage.getImage();
        Image newImg = img.getScaledInstance(imageLabel.getWidth(), imageLabel.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }
    
    
    private void uploadImagePanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_uploadImagePanelMouseClicked
       JFileChooser file = new JFileChooser();
       file.setCurrentDirectory(new File(System.getProperty("user.home")));
       FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "jpg", "jpeg", "gif", "png");
       file.addChoosableFileFilter(filter);
       int result = file.showSaveDialog(null);
       if(result==JFileChooser.APPROVE_OPTION){
           File selectedFile = file.getSelectedFile();
           path = selectedFile.getAbsolutePath();
           imageLabel.setIcon(ResizeImage(path));
       }
       else{
           JOptionPane.showMessageDialog(this, "No Image is Selected", "Message Dialog", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_uploadImagePanelMouseClicked

