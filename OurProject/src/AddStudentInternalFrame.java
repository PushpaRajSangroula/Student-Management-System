    private void jPanel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel5MouseClicked
       enteredRegID = jTextField1.getText();
       enteredFirstMiddleName = jTextField2.getText();
       enteredLastName = jTextField3.getText();
       enteredAdmissionYear = jComboBox1.getSelectedItem().toString();
       enteredGrade = jComboBox2.getSelectedItem().toString();
       enteredDisability = jComboBox3.getSelectedItem().toString();
       enteredMotherTongue = jComboBox4.getSelectedItem().toString();
       enteredEthnicity = jComboBox5.getSelectedItem().toString();
       enteredFatherName = jTextField7.getText();
       enteredMotherName = jTextField6.getText();
       
       setCurrentStatus = "Student is Currently Present at the school";
       
       //selectedDate = (Date) jDateChooser1.getDate();
       Date selectedDate = jDateChooser1.getDate();
       enteredAddress = jTextArea1.getText();
       
       if(jRadioButton1.isSelected()==true){
           enteredGender = jRadioButton1.getText();
       }
       else if(jRadioButton2.isSelected()==true){
           enteredGender = jRadioButton2.getText();
       }
       else{
           enteredGender = jRadioButton3.getText();
       }
       
       //checking the not null condition
      
           
           if(enteredRegID.equals("")||enteredFirstMiddleName.equals("")||enteredLastName.equals("")||enteredAdmissionYear.equals("Select From Here")||enteredGrade.equals("Select From Here")||enteredDisability.equals("Select From Here")||enteredMotherTongue.equals("Select From Here")||enteredEthnicity.equals("Select From Here")||enteredFatherName.equals("")||enteredMotherName.equals("")||enteredAddress.equals("")){
                JOptionPane.showMessageDialog(this, "All the Fields are Mandatory", "Warning Message", JOptionPane.WARNING_MESSAGE);
                
           }
        //checking other validations
           else if(jRadioButton1.isSelected()==false&&jRadioButton2.isSelected()==false&&jRadioButton3.isSelected()==false){
               JOptionPane.showMessageDialog(this, "Gender is NOT SELECTED", "Warning Message", JOptionPane.WARNING_MESSAGE);
           }
        
           else if(imageLabel.getIcon()==null){
               JOptionPane.showMessageDialog(this, "Image has not been selected yet", "Image Selection Error", JOptionPane.WARNING_MESSAGE);
           }
           
           else if(selectedDate==null){
               JOptionPane.showMessageDialog(this, "Date of Birth is NOT SELECTED");
           }
           //inserting data into the database
           else{
   
               SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
               enteredDOB = sdf.format(jDateChooser1.getDate());
               try{
                   pst1 = con.prepareStatement("SELECT * FROM studentpersonaldetails WHERE regid = ?");
                   pst1.setString(1, enteredRegID);
                   rs = pst1.executeQuery();
                   
                   if(rs.next()){
                       JOptionPane.showMessageDialog(this, "Registration ID " + enteredRegID + " already exists. Enter a new Reg ID", "Already Exist Errror", JOptionPane.ERROR_MESSAGE);
                   }
                   else{
                       pst = con.prepareStatement("INSERT INTO studentpersonaldetails VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)");
                   pst.setString(1, enteredRegID);
                   pst.setString(2, enteredFirstMiddleName);
                   pst.setString(3, enteredLastName);
                   pst.setString(4, enteredAdmissionYear);
                   pst.setString(5, enteredGrade);
                   pst.setString(6, enteredDisability);
                   pst.setString(7, enteredMotherTongue);
                   pst.setString(8, enteredGender);
                   pst.setString(9, enteredEthnicity);
                   pst.setString(10, enteredFatherName);
                   pst.setString(11, enteredMotherName);
                   pst.setString(12, enteredDOB);
                   pst.setString(13, enteredAddress);
                   
                   //uploading image
                    File fl = new File(path);
                    FileInputStream fis = new FileInputStream(fl);
                    pst.setBinaryStream(14, fis);
                    
                    pst.setString(15, setCurrentStatus);
                    
                    //Executing the query
                    pst.executeUpdate();
                    
                    
                    
                    //Displaying the insertion successful message
                    JOptionPane.showMessageDialog(this, "Data has been successfully inserted into the DataBase", "Successful insertion message", JOptionPane.PLAIN_MESSAGE);
                    
                    //setting after completion values to null
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jComboBox2.setSelectedIndex(0);
                    jComboBox3.setSelectedIndex(0);
                    jComboBox4.setSelectedIndex(0);
                    bg.clearSelection();
                    jComboBox5.setSelectedIndex(0);
                    jTextField7.setText("");
                    jTextField6.setText("");
                    jDateChooser1.setDate(null);
                    jTextArea1.setText("");
                    imageLabel.setIcon(null);
                    
                    jTextField1.requestFocus();
                    
                   }
 
               }catch(SQLException e){
                   e.getMessage();
               }catch(FileNotFoundException e){
                   e.getMessage();
               }
 
           }

    }//GEN-LAST:event_jPanel5MouseClicked
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    

